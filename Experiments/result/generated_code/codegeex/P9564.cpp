#include <stdio.h>

int main() {
    int A, B;
    scanf("%d %d", &A, &B);

    // 骰子面值为1, 2, 3, 4, 5, 6
    // 红色点数和黑色点数分别为：1, 0, 1, 0, 1, 1
    // 需要检查是否存在三个骰子，使得红色点数和为A，黑色点数和为B

    // 由于每个骰子的红色点数和黑色点数和为3（1+0+1+0+1+1）
    // 因此A和B的和必须是3的倍数，且A和B的差值必须是0或3
    if ((A + B) % 3 == 0 && (A - B) % 3 == 0) {
        printf("Yes\n");
    } else {
        printf("No\n");
    }

    return 0;
}