#include <stdio.h>

int main() {
    int n;
    scanf("%d", &n);

    // 初始情况下，每个基因组只有一对基元，所以总能量为n
    // 每次合并两个基因组时，产生一个额外的基元对，并且每个基因组中的每对基元会与另一个基因组中的每对基元两两聚合
    // 所以合并后的基因组基元对数量为原来两个基因组基元对数量之和加上1
    // 由于每次合并都使得基因组数量减1，最终只会剩下一个基因组
    // 所以总能量为n * (n - 1) / 2 + n

    printf("%d\n", n * (n - 1) / 2 + n);

    return 0;
}