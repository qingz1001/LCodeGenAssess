#include <stdio.h>

int main() {
    int n, m;
    scanf("%d %d", &n, &m);

    // 定义一个二维数组dp，dp[i][j]表示第i次传球后，球在第j个同学手里的方法数
    long long dp[31][31] = {0};

    // 初始状态：第0次传球后，球在小蛮手里，即第1个同学手里
    dp[0][0] = 1;

    // 动态规划计算每次传球后球的位置
    for (int i = 0; i < m; i++) {
        for (int j = 0; j < n; j++) {
            // 第i次传球后，球在第j个同学手里的方法数等于
            // 上一次传球后，球在左边的同学手里和球在右边的同学手里之和
            dp[i + 1][j] = dp[i][j] + dp[i][j - 1];
            // 防止数组越界，当j为0时，左边的同学是第n个同学
            if (j == 0) {
                dp[i + 1][j] += dp[i][n - 1];
            }
        }
    }

    // 输出第m次传球后，球回到小蛮手里的方法数
    printf("%lld\n", dp[m][0]);

    return 0;
}