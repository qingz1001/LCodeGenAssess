int countStars(char *pattern) {
    int count = 0;
    for (int i = 0; i < 7; i++) {
        for (int j = 0; j < 7; j++) {
            if (pattern[i * 7 + j] == '*') {
                count++;
            }
        }
    }
    return count;
}

